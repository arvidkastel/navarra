				<h1>
					Biografi <div>&nbsp;</div>
				</h1>
				<p>
					Navarra har sedan starten 2009 uppmärksammats för sin oemotståndliga energi och sitt
					genuint originella förhållande till nordisk folkmusik. Första albumet Nya Fönster
					nominerades till Manifestpriset för årets folkmusikalbum och pris för årets nykomling på
					Folk- och världsmusikgalan 2012. Recensenterna beskrev Nya Fönster som "ett av årets mest
					intressanta album" och skrev att "Navarra vill och lyckas öppna nya fönster till
					folkmusiken". Musiken är både komplex och enkel, ösig och ömsint, och de nyskrivna
					texterna har fått många att hitta in i en genre där traditionstexterna ibland känns
					främmande.
				</p>
				<p>
					Projektet tog sin början i Göteborg med Arvids och Sofias ambition att skriva musik
					tillsammans. Inspirationen kom från många håll, men hade tyngdpunkt i nordisk folkmusik.
					Språket skulle vara svenska. Efter ett par experiment med sättning föll allt på plats när
					Erika och Calle kom med i bandet, och Navarra fick sin nuvarande form. Bandet blev snabbt
					uppmärksammat för sina liveframträdanden, och har spelat på de flesta stora scener för
					folkmusik i Sverige och Norge.
				</p>
				<div class="row content-row">
					<div class="span3">
						<h2>Arvid Kästel</h2>
						<p>
							Pianist och civilingenjör med bakgrund inom jazz, pop och folk. Svänger som ingen
							annan och komponerar musik som inspirerar och influerar en ny generation folkmusiker.
							Spelar också med gruppen <a href="http://www.thewoods.se/">The Woods</a>, och
							spelmannen Mats Berglund.<br />
						</p>
					</div>

					<div class="span3">
						<h2>Sofia Kunze</h2>
						<p>
							Sångerska som trollbinder publiken med sin fullständiga scennärvaro. Hennes poetiska
							och berättande texter är mycket omtyckta och har gett ett nytt språk till den svenska
							folkmusiken. Sofia sjunger också i vokaltrion <a href="https://soundcloud.com/lyss-11"
								>Lyss</a
							>
							och anlitas som solist i olika körprojekt.<br /><a href="http://sofiabergstrom.se/"
								>Sofias hemsida</a
							>
						</p>
					</div>

					<div class="span3">
						<h2>Erika Risinger</h2>
						<p>
							Flitigt frilansande violinist och riksspelman med rötter i Dalarna och Skåne.
							Initiativtagare till kritikerrosade gruppen <a href="htp://www.sallyswag.se"
								>Sallyswag</a
							>
							och musikalisk ledare för folkmusikstorbandet World Wide Orchestra.
							<br /><a href="http://www.erikarisinger.se/">www.erikarisinger.se</a><br />
						</p>
					</div>

					<div class="span3">
						<h2>Carl-Johan Groth</h2>
						<p>
							Slagverkare med bakgrund i jazzen som med sitt driv och dynamik också utmärkt sig som
							en av Sveriges mest framstående slagverkare i folkmusikgenren. Spelar förutom med
							Navarra med den prisbelönta och kritikerrosade jazzgruppen <a
								href="https://www.farvel.se/">Farvel</a
							>.
